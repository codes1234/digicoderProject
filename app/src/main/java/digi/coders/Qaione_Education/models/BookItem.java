
package digi.coders.Qaione_Education.models;


public class BookItem {

    private String id;
    private String category;
    private String bookname;
    private String bookdescription;
    private String bookcoverpic;
    private String bookpdf;
    private String date;
    private String time;
    private String status;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getBookname() {
        return bookname;
    }

    public void setBookname(String bookname) {
        this.bookname = bookname;
    }

    public String getBookdescription() {
        return bookdescription;
    }

    public void setBookdescription(String bookdescription) {
        this.bookdescription = bookdescription;
    }

    public String getBookcoverpic() {
        return bookcoverpic;
    }

    public void setBookcoverpic(String bookcoverpic) {
        this.bookcoverpic = bookcoverpic;
    }

    public String getBookpdf() {
        return bookpdf;
    }

    public void setBookpdf(String bookpdf) {
        this.bookpdf = bookpdf;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

}
